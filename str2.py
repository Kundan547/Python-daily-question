#Negative Indexing

Shooping_Cart = ["Apple", "banana", "Kiwi", "Chips", "Oil", "FaceWash"]

#Before using the nbegative indexing
print(Shooping_Cart)


#After using negative indexing to print the last three items of shooping cart.
last_items = Shooping_Cart[-3:]

print(last_items)

#Also we can acceses the first three or two elements
first_items= Shooping_Cart[:-5]
print(first_items)